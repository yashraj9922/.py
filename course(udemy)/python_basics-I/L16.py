#  Escape Sequences
#  double quotes inside double quotes...single quotes inside single quotes

# \ symbol is used to escape the character after it...it is used to print the character after it

weather = " It\'s \"kind of\" sunny"
print(weather)

#  \t is used to print a tab
weather = "\t It\'s \"kind of\" sunny"
print(weather)

#  \n is used to print a new line
weather = "\t It\'s \"kind of\" sunny \n Hope you have a good day"
print(weather)
